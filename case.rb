weather = 'windy'

case weather
    when 'rainy'
    puts "Bring an umbrella and wear your rain boots."
    when 'windy'
    puts "I think a North Face is in order."
    when 'sunny'
    puts "It's pool season!"
    when 'cloudy'
    puts "It might be a little chilly. It's San Francisco weather!"
    when 'snowy'
    puts "Snowboarding season!"
    else
    puts "Hmmm....maybe bring layers just in case?"
end